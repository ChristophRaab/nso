# DDC MMD Loss
# Tzeng, Eric, et al. "Deep domain confusion: Maximizing for domain invariance." arXiv preprint arXiv:1412.3474 (2014).
# Origin of Source Code: https://github.com/jindongwang/transferlearning/tree/master/code/deep/DDC_DeepCoral
def linear_mmd2( f_of_X, f_of_Y):
    loss = 0.0
    delta = f_of_X - f_of_Y
    loss = torch.mean((delta[:-1] * delta[1:]).sum(1))
    return loss

def DDC_MMD(source, target, kernel_type='rbf', kernel_mul=2.0, kernel_num=5,fix_sigma = None):
    if kernel_type == 'linear':
        return linear_mmd2(source, target)
    elif kernel_type == 'rbf':
        batch_size = int(source.size()[0])
        kernels = guassian_kernel(
            source, target, kernel_mul=kernel_mul, kernel_num=kernel_num, fix_sigma=fix_sigma)
        with torch.no_grad():
            XX = torch.mean(kernels[:batch_size, :batch_size])
            YY = torch.mean(kernels[batch_size:, batch_size:])
            XY = torch.mean(kernels[:batch_size, batch_size:])
            YX = torch.mean(kernels[batch_size:, :batch_size])
            loss = torch.mean(XX + YY - XY - YX)
        torch.cuda.empty_cache()
        return loss


